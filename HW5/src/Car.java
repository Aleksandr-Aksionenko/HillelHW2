public class Car {

    int id;
    String brand;
    String model;
    int year;
    String colour;
    int price;
    String registrationNumber;

    public Car(int id, String brand, String model, int year, String colour, int price, String registrationNumber) {
        this.id = id;
        this.brand = brand;
        this.model = model;
        this.year = year;
        this.colour = colour;
        this.price = price;
        this.registrationNumber = registrationNumber;
    }
}
